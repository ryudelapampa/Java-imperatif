package fr.declaration.variable;

public class switchCase {

	public static void main(String[] args) {
		
		int i = 18;
		
		switch (i) {
			case 10:
				System.out.println("i vaut 10");
				break;
			case 15:
				System.out.println("i vaut 15");
				break;
			default:
				System.out.println("i vaut ni 10 ni 15");
				break;
		}
	}

}
